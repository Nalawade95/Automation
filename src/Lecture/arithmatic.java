package Lecture;

public class arithmatic {
public static void main(String[] args){
	int a = 20;
	int b = 10;
	int result;
	result = a+b;
	System.out.println(result);//30
	
	result = a-b;
	System.out.println(result);//10
	
	result = a*b;
	System.out.println(result);//200
	
	result = a/b;
	System.out.println(result);//2
	
}
}
